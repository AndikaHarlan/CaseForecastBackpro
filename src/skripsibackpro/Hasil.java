    private void btnRamalanBulananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRamalanBulananActionPerformed
        // TODO add your handling code here:
        String bulan = cmbBulan.getSelectedItem().toString();
        String tahun = cmbTahun.getSelectedItem().toString();
        
        Data data = new Data(jenisPenyakit);
        double[][] dataRamalBulanan = data.getDataRamalBulanan(bulan, tahun, panjangData);
        Backpro backpro = new Backpro(panjangData, alfa);
        
        double[][] bobotHidden = new double[bobotBaru.length - 1][bobotBaru[0].length - 1];
        double[] bobotOutput = new double[bobotBaru.length - 1];
        double[] bias = bobotBaru[bobotBaru.length - 1];

        for (int i = 0; i < bobotHidden.length; i++) {
            for (int j = 0; j < bobotHidden[0].length; j++) {
                bobotHidden[i][j] = bobotBaru[i][j];
            }
        }
        for (int i = 0; i < bobotOutput.length; i++) {
            bobotOutput[i] = bobotBaru[i][panjangData];
        }
        double denor = backpro.deNormalisasi(backpro.feedForward(dataRamalBulanan, 0, bobotHidden, bias, bobotOutput), dataRamalBulanan);
        
        model3 = new DefaultTableModel();
        model3.addColumn("Data Aktual");
        model3.addColumn("Hasil Ramalan");
        tblHasilRamal.setModel(model3);
        model3.addRow(new Object[]{
            dataRamalBulanan[0][panjangData],denor
        });
        
    }//GEN-LAST:event_btnRamalanBulananActionPerformed

