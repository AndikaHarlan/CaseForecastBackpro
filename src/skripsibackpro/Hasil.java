    public Hasil(double[] dataAsli, double[] denor, double[][] bobotBaru, double mse, double akurat, String jenisPenyakit, int panjangData, double alfa) {
        initComponents();
        this.bobotBaru = bobotBaru;
        this.jenisPenyakit = jenisPenyakit;
        this.panjangData = panjangData;
        this.alfa = alfa;
        //PERBANDINGAN
        model1 = new DefaultTableModel();
        model1.addColumn("Data Aktual");
        model1.addColumn("Hasil Peramalan");
        tblHasilPerbData.setModel(model1);
        for (int i = 0; i < dataAsli.length; i++) {
            model1.addRow(new Object[]{
                dataAsli[i], denor[i]
            });
        }
        //BOBOT
        model2 = new DefaultTableModel();
        model2.addColumn("Bobot");
        for (int i = 0; i < bobotBaru.length - 1; i++) {
            model2.addColumn("v" + (i + 1));
        }
        model2.addColumn("w1");
        tblBobotBiasOptimal.setModel(model2);
        Object[][] obj = new Object[bobotBaru.length][bobotBaru[0].length + 1];
        for (int i = 0; i < obj.length; i++) {
            for (int j = 1; j < obj[0].length; j++) {
                obj[i][j] = bobotBaru[i][j - 1];
            }
            if (i == obj.length - 1) {
                obj[i][0] = "Bias";
            } else {
                obj[i][0] = "x" + (i + 1);
            }
        }
        for (int i = 0; i < bobotBaru.length; i++) {
            model2.addRow(obj[i]);
        }
        //MSE
        txtMSE.setText(String.valueOf(mse));
        //Tingkat Keakuratan
        txtAkurat.setText(String.valueOf(akurat));
    }

